    private void cariBuku(String a) {
        DefaultTableModel barang = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
       }; 
       
           
       barang.addColumn("Id");
       barang.addColumn("Nama");
       barang.addColumn("Tipe");
       barang.addColumn("Harga");
       barang.addColumn("Jumlah");       
       
       jTable1.setModel(barang);
       
        try {
            String query = "SELECT * FROM barang WHERE nama LIKE '%" + a + "%'";
            Statement stm = cn.createStatement();
            ResultSet rs = stm.executeQuery(query);
            while(rs.next()){      
                barang.addRow(new Object[]{rs.getString("id"),
                rs.getString("nama"), 
                rs.getString("tipe"), 
                rs.getString("harga"),
                rs.getString("jumlah")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(admin1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
