    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String id = txtID.getText();
        String nama = txtNama.getText();
        String tipe = txtTipe.getText();
        String jumlah = txtJumlah.getText();
        String harga = txtHarga.getText();
        cn = null;
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/uasjava","root","");
            pst = cn.prepareStatement("insert into barang values(?,?,?,?,?)");
            pst.setString(1, id);
            pst.setString(2, nama);
            pst.setString(3, tipe);
            pst.setString(4, jumlah);
            pst.setString(5, harga);
            int i = pst.executeUpdate();
            if(i>0){
                JOptionPane.showMessageDialog(null, "data is save");
            }else{
                JOptionPane.showMessageDialog(null, "data is not save");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"");
        }

    }//GEN-LAST:event_btnInsertActionPerformed
